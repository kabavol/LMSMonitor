void putTapeType(audio_t audio) {

    const uint8_t cdata[] = {
        0xcc, 0x44, 0x44, 0x44, 0x30, 0x10, 
        0x10, 0x10, 0xd4, 0x54, 0x54, 0x48,
    };
    int szw = 6;
    int szh = 4;
    int w = elementLength(szh, szw);

    int start = 0;
    uint8_t dest[w];
    start = audio.audioIcon * w;
    int x = 99;
    int y = 29;
    memcpy(dest, cdata + start, sizeof dest);
    fillRectangle(x, y, szw, szh, BLACK);
    drawBitmap(x, y, dest, szw, szh, WHITE);
}

